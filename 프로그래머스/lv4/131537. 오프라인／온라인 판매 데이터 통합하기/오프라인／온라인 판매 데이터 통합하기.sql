SELECT TO_CHAR(SALES_DATE,'YYYY-MM-DD') AS SALES_DATE
      ,PRODUCT_ID
      ,USER_ID
      ,SALES_AMOUNT
FROM ONLINE_SALE
WHERE SALES_DATE BETWEEN TO_DATE('2022-03-01 00:00:00','YYYY-MM-DD HH24:MI:SS')
AND TO_DATE('2022-03-31 23:59:59','YYYY-MM-DD HH24:MI:SS')
UNION ALL
SELECT TO_CHAR(SALES_DATE,'YYYY-MM-DD') AS SALES_DATE
      ,PRODUCT_ID
      ,NULL AS USER_ID
      ,SALES_AMOUNT
FROM OFFLINE_SALE
WHERE SALES_DATE BETWEEN TO_DATE('2022-03-01 00:00:00','YYYY-MM-DD HH24:MI:SS')
AND TO_DATE('2022-03-31 23:59:59','YYYY-MM-DD HH24:MI:SS')
ORDER BY SALES_DATE,PRODUCT_ID,USER_ID;